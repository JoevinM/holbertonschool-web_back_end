#!/usr/bin/env python3

"""
This module defines a type-annotated function `to_str` that takes a float
as an argument and returns its string representation. This exercise is part
of learning basic type annotations in Python.
"""


def to_str(n: float) -> str:

    """
    Convert a float value to its string representation.

    Args:
        n (float): The float number to convert.

    Returns:
        str: The string representation of the float."""


    return str(n)
